Map.prototype.spawnTiles = function()
{
  for (var xt = 0; xt < 16 * this.tileSizeX; xt += this.tileSizeX) {
    for (var yt = 0; yt < 50 * this.tileSizeY; yt += this.tileSizeY) {
      var tile = this.game.add.sprite(xt, yt, 'tile', 0, this.group);
        tile.anchor.setTo(0.5);
      tile.inputEnabled = true;
      tile.walled = false;
    }
  }
}

Map.prototype.setObstacle = function(tileY, tileX)
{
    this.grid[tileX / this.tileSizeY][tileY / this.tileSizeX] = 1;
}

Map.prototype.constructor = Map;
